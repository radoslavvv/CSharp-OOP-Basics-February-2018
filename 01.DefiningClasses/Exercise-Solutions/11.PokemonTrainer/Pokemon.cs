public class Pokemon
{
    private string name;
    private string element;
    private double health;

    public string Name
    {
        get => name;
        set => name = value;
    }

    public string Element
    {
        get => element;
        set => element = value;
    }

    public double Health
    {
        get => health;
        set => health = value;
    }
}
